// Null e Undefined

var vazio = null
var igualNull = vazio == undefined
var estritamenteIgualNull = vazio === undefined
console.log('Igual a null: ', igualNull, '\n', 'Estritamente igual a null: ', estritamenteIgualNull);

var naoDefinida
var igual = naoDefinida == undefined
var estritamenteIgual = naoDefinida === undefined
console.log('Igual a undefined: ', igual, '\n', 'Estritamente igual a undefined: ', estritamenteIgual);
